#include <stdio.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return (i);
}

int	main(void)
{
	char	string[] = "abcdef";

	int	count = ft_strlen(string);
	printf("Caracteres: %d", count);
}
